package com.info.demo.config;

public class MCbsApiChannel {
	
	private String userId; 
	private String userPassword;
	private String exchangeCode;
	private String exchangeName;
	private String ipAddress;
	private String requestUserId;
	private String cbsApiChannel;
	public String getUserId() {
		return userId;
	}
	public void setUserId(String userId) {
		this.userId = userId;
	}
	public String getUserPassword() {
		return userPassword;
	}
	public void setUserPassword(String userPassword) {
		this.userPassword = userPassword;
	}
	public String getExchangeCode() {
		return exchangeCode;
	}
	public void setExchangeCode(String exchangeCode) {
		this.exchangeCode = exchangeCode;
	}
	public String getExchangeName() {
		return exchangeName;
	}
	public void setExchangeName(String exchangeName) {
		this.exchangeName = exchangeName;
	}
	public String getIpAddress() {
		return ipAddress;
	}
	public void setIpAddress(String ipAddress) {
		this.ipAddress = ipAddress;
	}
	public String getRequestUserId() {
		return requestUserId;
	}
	public void setRequestUserId(String requestUserId) {
		this.requestUserId = requestUserId;
	}
	public String getCbsApiChannel() {
		return cbsApiChannel;
	}
	public void setCbsApiChannel(String cbsApiChannel) {
		this.cbsApiChannel = cbsApiChannel;
	}
	

}
